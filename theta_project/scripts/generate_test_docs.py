def generate_long_document(template: str, multiplier: int = 5) -> str:
    """生成超长文档内容 (重复模板内容)"""
    return (template.strip() + "\n\n") * multiplier

def create_test_documents(output_dir: str, num_docs: int = 5):
    """创建测试文档"""
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)
    
    doc_names = [
        "海南自由贸易港建设总体方案_测试.docx",
        "广东省数字经济发展规划_测试.docx",
        "河北省生态环境保护规划_测试.docx",
        "湖南省乡村振兴战略规划_测试.docx",
        "黑龙江省现代农业发展规划_测试.docx",
    ]
    
    created_files = []
    
    for i in range(min(num_docs, len(BASE_TEXT_TEMPLATES))):
        template = BASE_TEXT_TEMPLATES[i]
        doc_name = doc_names[i]
        
        # 生成超长内容 (5 倍重复，确保 > 5000 字)
        long_content = generate_long_document(template, multiplier=5)
        char_count = len(long_content)
        
        # 创建 Word 文档
        doc = Document()
        
        # 添加标题
        title = doc_name.replace("_测试.docx", "")
        doc.add_heading(title, level=0)
        
        # 添加内容
        for para in long_content.split("\n\n"):
            if para.strip():
                p = doc.add_paragraph(para.strip())
                p.style.font.size = Pt(12)
        
        # 保存文档
        doc_path = output_path / doc_name
        doc.save(str(doc_path))
        
        created_files.append({
            "name": doc_name,
            "path": str(doc_path),
            "char_count": char_count
        })
        
        print(f"  ✓ 创建: {doc_name} ({char_count} 字)")
    
    return created_files

